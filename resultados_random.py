from random import choice as elegir

def generar_resultado(trues: int, falses: int) -> bool:
    lista_trues = [True] * trues
    lista_falses = [False] * falses
    lista_opciones = lista_trues + lista_falses
    resultado = elegir(lista_opciones)
    return resultado
